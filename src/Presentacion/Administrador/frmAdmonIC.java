    private void btnAgregarRegistrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarRegistrosActionPerformed

        accion = "agregar";

        if (txtId.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un identificador");
            txtId.requestFocus();
            return;
        }

        if (txtNom.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre");
            txtNom.requestFocus();
            return;
        }

        if (txtDes.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripcion");
            txtDes.requestFocus();
            return;
        }

        if (txtHorario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un horario");
            txtHorario.requestFocus();
            return;
        }

        if (seInsertoImagen == false) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una imagen");
            btnExaminar.requestFocus();
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estas seguro de agregar el registro?", "Confirmar", 2);

        if (confirmacion == 0) {

            dIntereses_Culturales dts = new dIntereses_Culturales();
            fInteresesCulturales func = new fInteresesCulturales();

            dts.setId_Interes_Cultural(txtId.getText());
            dts.setDescripcion_Interes_Cultural(txtDes.getText());
            dts.setNombre_Interes_Cultural(txtNom.getText());
            dts.setHorario_Interes_Cultural(txtHorario.getText());
            dts.setDireccion_Interes_Cultural(txtDir.getText());
            dts.setImagen_Interes_Cultural(ImagenBinaria);
            dts.setId_Tipo_Interes(TipoIC);
            dts.setId_Municipio(IdMunicipio);

            func.insertar(dts);

            mostrarDatos("", 1);
            limpiarCampos();

            JOptionPane.showConfirmDialog(rootPane, "Dato Ingresado correctamente...");
            seInsertoImagen = false;

        }


    }//GEN-LAST:event_btnAgregarRegistrosActionPerformed

