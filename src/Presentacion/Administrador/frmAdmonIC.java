    private void tblMostrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMostrarMouseClicked

        int fila = tblMostrar.rowAtPoint(evt.getPoint());

        //       fInteresesCulturales func = new fInteresesCulturales();
        //     dIntereses_Culturales dts = new dIntereses_Culturales();
        txtId.setText(tblMostrar.getValueAt(fila, 0).toString());
        txtNom.setText(tblMostrar.getValueAt(fila, 1).toString());
        txtDes.setText(tblMostrar.getValueAt(fila, 2).toString());
        txtHorario.setText(tblMostrar.getValueAt(fila, 3).toString());
        txtDir.setText(tblMostrar.getValueAt(fila, 4).toString());

        String tipo = tblMostrar.getValueAt(fila, 5).toString();
        String municipio = tblMostrar.getValueAt(fila, 6).toString();
        String region = tblMostrar.getValueAt(fila, 7).toString();

        cobTipo.setSelectedItem(tipo);
        cobMunicipio.setSelectedItem(municipio);
        cobRegion.setSelectedItem(region);
        
        try {

            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT imagen_interes_cultural FROM intereses_culturales "
                                            + "WHERE id_interes_cultural = '" + txtId.getText() + "';");

            ImageIcon ii = null;
            InputStream is = null;
            rs.next();
            is = rs.getBinaryStream(1);
            BufferedImage bi = ImageIO.read(is);
            ii = new ImageIcon(bi.getScaledInstance(228, 128, 228));
            this.lblImagen.setIcon(ii);

        } catch (Exception e) {

        }

    }//GEN-LAST:event_tblMostrarMouseClicked

